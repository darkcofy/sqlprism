@cli.command("init")
@click.option(
    "--format",
    "fmt",
    type=click.Choice(["yaml", "json"], case_sensitive=False),
    default="yaml",
    help="Config file format (default: yaml)",
)
def init_config(fmt: str):
    """Create a default config file in the working directory."""
    cwd = Path.cwd()

    # Check for any existing config variant to avoid shadowing
    for name in _CONFIG_NAMES:
        existing = cwd / name
        if existing.exists():
            click.echo(f"Config already exists at {existing}")
            return

    ext = ".yml" if fmt == "yaml" else ".json"
    config_file = cwd / f"sqlprism{ext}"

    default_config = {
        "db_path": str(DEFAULT_DB_PATH),
        "sql_dialect": None,
        "repos": {
            "my-project": {
                "path": str(Path.cwd()),
                "dialect": None,
                "dialect_overrides": {
                    "# athena/": "athena",
                    "# starrocks/": "starrocks",
                },
            },
        },
        "sqlmesh_repos": {
            "# my-sqlmesh-project": {
                "project_path": "/path/to/sqlmesh/folder",
                "env_file": "/path/to/.env",
                "dialect": "athena",
                "variables": {"GRACE_PERIOD": 7},
            },
        },
        "dbt_repos": {
            "# my-dbt-project": {
                "project_path": "/path/to/dbt/project",
                "env_file": "/path/to/.env",
                "target": "dev",
                "dialect": "starrocks",
                "dbt_command": "uv run dbt",
            },
        },
    }

    if fmt == "yaml":
        config_file.write_text(
            yaml.dump(default_config, default_flow_style=False, sort_keys=False)
        )
    else:
        config_file.write_text(json.dumps(default_config, indent=2))
    click.echo(f"Created config at {config_file}")
    click.echo("Edit it to add your repos, then run: sqlprism reindex")


