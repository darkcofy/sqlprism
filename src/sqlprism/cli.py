DEFAULT_DB_PATH = Path.home() / ".sqlprism" / "graph.duckdb"
LEGACY_CONFIG_PATH = Path.home() / ".sqlprism" / "config.json"

# Discovery order for config files in the working directory
_CONFIG_NAMES = ("sqlprism.yml", "sqlprism.yaml", "sqlprism.json")


# ---------------------------------------------------------------------------
# Config loading
# ---------------------------------------------------------------------------

def load_config(path: str | None = None) -> dict:
    """Load config from YAML or JSON file with automatic discovery.

    Discovery order when *path* is ``None``:

    1. ``sqlprism.yml`` in the working directory
    2. ``sqlprism.yaml`` in the working directory
    3. ``sqlprism.json`` in the working directory
    4. ``~/.sqlprism/config.json`` (legacy location)

    Args:
        path: Explicit config file path.  When provided, only that
            file is tried (no discovery).

    Returns:
        Parsed config dict.

    Raises:
        FileNotFoundError: No config file found in any location.
    """
    if path is not None:
        resolved = Path(path)
        if not resolved.exists():
            raise FileNotFoundError(
                f"Config file not found: {resolved}\n"
                "Create one with: sqlprism init"
            )
        return _parse_config_file(resolved)

    cwd = Path.cwd()
    for name in _CONFIG_NAMES:
        candidate = cwd / name
        if candidate.exists():
            return _parse_config_file(candidate)

    if LEGACY_CONFIG_PATH.exists():
        return _parse_config_file(LEGACY_CONFIG_PATH)

    raise FileNotFoundError(
        "No config file found. Searched:\n"
        + "".join(f"  - {cwd / n}\n" for n in _CONFIG_NAMES)
        + f"  - {LEGACY_CONFIG_PATH}\n"
        + "Create one with: sqlprism init"
    )


def _parse_config_file(path: Path) -> dict:
    """Parse a YAML or JSON config file."""
    suffix = path.suffix.lower()
    if suffix not in (".yml", ".yaml", ".json"):
        raise click.ClickException(
            f"Unsupported config format: {path.suffix}. Use .yml, .yaml, or .json"
        )
    try:
        text = path.read_text()
        if suffix in (".yml", ".yaml"):
            result = yaml.safe_load(text) or {}
        else:
            result = json.loads(text) if text.strip() else {}
    except (yaml.YAMLError, json.JSONDecodeError, OSError) as e:
        raise click.ClickException(f"Failed to parse {path}: {e}") from e
    if not isinstance(result, dict):
        raise click.ClickException(
            f"Config must be a mapping, got {type(result).__name__} in {path}"
        )
    return result


def _try_load_config(path: str | None) -> dict:
    """Try to load config, return empty dict if not found.

    Unlike ``_cli_load_config``, does not raise on missing config.
    Used by commands that work with just ``--db`` and no config.
    """
    try:
        return load_config(path)
    except FileNotFoundError:
        return {}


def _cli_load_config(path: str | None) -> dict:
    """CLI wrapper: converts FileNotFoundError to a friendly click error."""
    try:
        return load_config(path)
    except FileNotFoundError as e:
        raise click.ClickException(str(e)) from e


# ---------------------------------------------------------------------------
# Repo-config merging
# ---------------------------------------------------------------------------

# (config_key, repo_type, path_key). path_key is None for "sql" because those
# configs already carry a "path" field directly; for dbt/sqlmesh the path is
# mirrored from "project_path".
_REPO_TYPE_SPECS: tuple[tuple[str, str, str | None], ...] = (
    ("repos", "sql", None),
    ("dbt_repos", "dbt", "project_path"),
    ("sqlmesh_repos", "sqlmesh", "project_path"),
)


def _build_repo_configs(config: dict) -> dict:
    """Merge repos, dbt_repos, sqlmesh_repos into a single dict with repo_type tags.

    Preserves full config for dbt/sqlmesh so reindex_files() can access
    renderer params (profiles_dir, env_file, target, variables, etc.).

    Entries whose name starts with ``#`` are treated as YAML-template comments
    and skipped — these come from the default config generated by
    ``sqlprism init`` and are never meant to be indexed.
    """
    result: dict[str, dict] = {}
    for config_key, repo_type, path_key in _REPO_TYPE_SPECS:
        for name, cfg in config.get(config_key, {}).items():
            if name.startswith("#"):
                continue
            if isinstance(cfg, dict):
                entry = {**cfg, "repo_type": repo_type}
                if path_key is not None:
                    entry["path"] = cfg[path_key]
                result[name] = entry
            elif path_key is None:
                # Short form is only supported for SQL: {name: "/path/to/repo"}.
                result[name] = {"path": cfg, "repo_type": repo_type}
            else:
                raise click.ClickException(
                    f"{repo_type} repo '{name}' must be a mapping with "
                    f"'{path_key}', got {type(cfg).__name__}"
                )
    return result


# ---------------------------------------------------------------------------
# GraphDB open helpers
# ---------------------------------------------------------------------------

@contextmanager
def _open_graph_for_write(
    config_path: str | None,
    db_path: str | None,
) -> Iterator[tuple["GraphDB", dict, str]]:
    """Load config, ensure DB parent dir exists, open GraphDB for a write command.

    Yields ``(graph, config, effective_db_path)`` and closes the graph on exit.
    """
    from sqlprism.core.graph import GraphDB

    config = _cli_load_config(config_path)
    effective_db_path = db_path or config.get("db_path", str(DEFAULT_DB_PATH))
    Path(effective_db_path).parent.mkdir(parents=True, exist_ok=True)
    with GraphDB(effective_db_path) as graph:
        yield graph, config, effective_db_path


@contextmanager
def _open_graph_for_read(
    config_path: str | None,
    db_path: str | None,
    *,
    require_config: bool = True,
    missing_on_stderr: bool = True,
) -> Iterator[tuple["GraphDB", dict, str]]:
    """Load config, verify DB exists, open GraphDB for a read-only command.

    Yields ``(graph, config, effective_db_path)`` and closes the graph on exit.
    Exits with code 1 when the DB does not exist.

    ``require_config=False`` tolerates a missing config file (for commands
    that can run with just ``--db``). ``missing_on_stderr=False`` preserves
    legacy commands that wrote the "No index found" message to stdout.
    """
    from sqlprism.core.graph import GraphDB

    loader = _cli_load_config if require_config else _try_load_config
    config = loader(config_path)
    effective_db_path = db_path or config.get("db_path", str(DEFAULT_DB_PATH))
    if not Path(effective_db_path).exists():
        click.echo(
            "No index found. Run 'sqlprism reindex' first.",
            err=missing_on_stderr,
        )
        sys.exit(1)
    with GraphDB(effective_db_path) as graph:
        yield graph, config, effective_db_path


