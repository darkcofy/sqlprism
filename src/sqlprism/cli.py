@click.group()
@click.option(
    "--log-level",
    type=click.Choice(["DEBUG", "INFO", "WARNING", "ERROR"], case_sensitive=False),
    default="WARNING",
    help="Set logging verbosity",
)
@click.pass_context
def cli(ctx, log_level):
    """SQLPrism — SQL knowledge graph for your codebase."""
    ctx.ensure_object(dict)
    logging.basicConfig(
        level=getattr(logging, log_level.upper()),
        format="%(asctime)s %(levelname)s %(name)s: %(message)s",
        datefmt="%Y-%m-%dT%H:%M:%S",
    )


@cli.command()
@click.option(
    "--config",
    "config_path",
    type=click.Path(),
    default=None,
    help="Path to config file",
)
@click.option(
    "--db",
    "db_path",
    type=click.Path(),
    default=None,
    help="Path to DuckDB file (overrides config)",
)
@click.option(
    "--transport",
    type=click.Choice(["stdio", "streamable-http"]),
    default="stdio",
    help="MCP transport mode",
)
@click.option("--port", type=int, default=8000, help="Port for HTTP transport")
def serve(config_path: str, db_path: str | None, transport: str, port: int):
    """Start the MCP server."""
    # Servers need more visibility — override to INFO unless already more verbose
    root_logger = logging.getLogger()
    if root_logger.level > logging.INFO:
        logging.basicConfig(
            level=logging.INFO,
            force=True,
            format="%(asctime)s %(levelname)s %(name)s: %(message)s",
            datefmt="%Y-%m-%dT%H:%M:%S",
        )
    config = _cli_load_config(config_path)

    effective_db_path = db_path or config.get("db_path", str(DEFAULT_DB_PATH))

    Path(effective_db_path).parent.mkdir(parents=True, exist_ok=True)

    all_repos = _build_repo_configs(config)

    configure(
        db_path=effective_db_path,
        repos=all_repos,
        sql_dialect=config.get("sql_dialect"),
    )

    if transport == "stdio":
        mcp.run()
    else:
        mcp.settings.port = port
        mcp.run(transport="streamable-http")


@cli.command()
@click.option("--config", "config_path", type=click.Path(), default=None)
@click.option("--db", "db_path", type=click.Path(), default=None)
@click.option("--repo", "repo_name", type=str, default=None, help="Reindex a specific repo only")
def reindex(config_path: str, db_path: str | None, repo_name: str | None):
    """Run a manual reindex from the command line."""
    from sqlprism.core.indexer import Indexer

    all_parse_errors: list[str] = []

    with _open_graph_for_write(config_path, db_path) as (graph, config, _):
        indexer = Indexer(graph)
        all_repos = _build_repo_configs(config)

        if repo_name:
            if repo_name not in all_repos:
                click.echo(f"Error: repo '{repo_name}' not in config", err=True)
                sys.exit(1)
            all_repos = {repo_name: all_repos[repo_name]}

        sql_dialect = config.get("sql_dialect")
        for name, cfg in all_repos.items():
            handler = _REINDEX_HANDLERS[cfg["repo_type"]]
            all_parse_errors.extend(handler(indexer, name, cfg, sql_dialect))

    if all_parse_errors:
        click.echo(f"\n{len(all_parse_errors)} parse error(s):", err=True)
        for err in all_parse_errors:
            click.echo(f"  {err}", err=True)
        sys.exit(1)

    click.echo("Done.")


@cli.command("reindex-file")
@click.argument("paths", nargs=-1, required=True, type=click.Path())
@click.option("--config", "config_path", type=click.Path(), default=None)
@click.option("--db", "db_path", type=click.Path(), default=None)
def reindex_file(paths, config_path, db_path):
    """Reindex specific files (fast on-save path).

    Accepts one or more file paths. Resolves each to its repo,
    determines repo type, and reindexes only the affected models.
    Deleted file paths trigger removal of stale nodes from the graph.

    For editors without MCP integration:
      vim: autocmd BufWritePost *.sql silent !sqlprism reindex-file %:p
    """
    from sqlprism.core.indexer import Indexer

    with _open_graph_for_write(config_path, db_path) as (graph, config, _):
        repo_configs = _build_repo_configs(config)
        indexer = Indexer(graph)

        # Register repos so reindex_files can resolve paths
        for name, cfg in repo_configs.items():
            graph.upsert_repo(name, cfg["path"], repo_type=cfg["repo_type"])

        resolved_paths: list[str | Path] = [str(Path(p).resolve()) for p in paths]
        stats = indexer.reindex_files(paths=resolved_paths, repo_configs=repo_configs)

    click.echo(
        f"reindexed={stats['reindexed']}, "
        f"skipped={stats['skipped']}, "
        f"deleted={stats['deleted']}"
    )
    if stats["errors"]:
        click.echo(f"{len(stats['errors'])} error(s):", err=True)
        for err in stats["errors"]:
            click.echo(f"  {err}", err=True)
        sys.exit(1)


@cli.command("reindex-sqlmesh")
@click.option("--config", "config_path", type=click.Path(), default=None)
@click.option("--db", "db_path", type=click.Path(), default=None)
@click.option("--name", "repo_name", type=str, required=True, help="Repo name for the index")
@click.option(
    "--project",
    "project_path",
    type=click.Path(exists=True),
    required=True,
    help="Path to sqlmesh project dir (containing config.yaml)",
)
@click.option(
    "--env-file",
    type=click.Path(exists=True),
    default=None,
    help="Path to .env file for sqlmesh config",
)
@click.option("--dialect", type=str, default="athena", help="SQL dialect (default: athena)")
@click.option(
    "--var",
    "variables",
    type=(str, str),
    multiple=True,
    help="SQLMesh variables as key value pairs, e.g. --var GRACE_PERIOD 7",
)
@click.option(
    "--sqlmesh-command",
    type=str,
    default="uv run python",
    help="Command to run python in sqlmesh venv (default: 'uv run python')",
)
def reindex_sqlmesh(
    config_path: str,
    db_path: str | None,
    repo_name: str,
    project_path: str,
    env_file: str | None,
    dialect: str,
    variables: tuple[tuple[str, str], ...],
    sqlmesh_command: str,
):
    """Index a sqlmesh project by rendering all models."""
    from sqlprism.core.indexer import Indexer

    var_dict: dict[str, str | int] = {}
    for k, v in variables:
        try:
            var_dict[k] = int(v)
        except ValueError:
            var_dict[k] = v

    with _open_graph_for_write(config_path, db_path) as (graph, _, _):
        indexer = Indexer(graph)
        click.echo(f"Rendering sqlmesh models from {project_path}...")
        stats = indexer.reindex_sqlmesh(
            repo_name=repo_name,
            project_path=project_path,
            env_file=env_file,
            variables=var_dict,
            dialect=dialect,
            sqlmesh_command=sqlmesh_command,
        )
        skipped = stats.get('models_skipped', 0)
        removed = stats.get('models_removed', 0)
        cached = stats.get('render_cached', False)
        skip_info = f", skipped={skipped}" if skipped else ""
        remove_info = f", removed={removed}" if removed else ""
        cache_info = " (render cached)" if cached else ""
        click.echo(
            f"  models={stats['models_rendered']}{skip_info}{remove_info}{cache_info}, "
            f"nodes={stats['nodes_added']}, "
            f"edges={stats['edges_added']}, "
            f"column_usage={stats['column_usage_added']}"
        )

    click.echo("Done.")


